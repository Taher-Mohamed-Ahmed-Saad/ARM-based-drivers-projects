#ifndef CRC_REG_H_
#define CRC_REG_H_

#define CRC_BASE_ADDRESS								0x40023000

/* ************************************************************************************************************ */
											/* CRC registers */
/* ************************************************************************************************************ */

typedef struct
{
  volatile uint32 DR;
  volatile uint8  IDR;
  uint8   RESERVED0;
  uint16  RESERVED1;
  volatile uint32 CR;
} CRC_TypeDef;


/* ************************************************************************************************************ */

#define CRC												((CRC_TypeDef	*)	CRC_BASE_ADDRESS)




#endif /* CRC_REG_H_ */
